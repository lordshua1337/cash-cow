'use client'

import { useState, useEffect, useCallback, useRef } from 'react'
import { useParams, useRouter } from 'next/navigation'
import {
  ArrowLeft,
  Loader2,
  Copy,
  Check,
  Clock,
  Calendar,
  CalendarRange,
  Wrench,
  Users,
  Lightbulb,
  Layers,
  ListOrdered,
  AlertTriangle,
  Rocket,
  Terminal,
  Sparkles,
  Pencil,
  RefreshCw,
  Send,
  X,
} from 'lucide-react'
import type { ProductIdea, BuildSpec } from '@/lib/types'
import {
  getTempIdea,
  getFavoriteById,
  addFavorite,
  getCachedSpec,
  cacheSpec,
} from '@/lib/favorites'

const COMPLEXITY_CONFIG = {
  weekend: { label: 'Weekend Project', icon: Clock, color: 'var(--cash)', bg: 'var(--cash-soft)' },
  'few-weeks': { label: 'A Few Weeks', icon: Calendar, color: 'var(--gold)', bg: 'var(--gold-soft)' },
  'month-plus': { label: 'A Month+', icon: CalendarRange, color: 'var(--blue)', bg: 'var(--blue-soft)' },
} as const

type SectionKey =
  | 'whatYoureBuilding'
  | 'whoWantsThis'
  | 'whyThisCouldWork'
  | 'coreFeatures'
  | 'techStack'
  | 'buildPlan'
  | 'v2Ideas'
  | 'risks'

const SECTION_META: Record<SectionKey, { title: string; icon: React.ElementType; color: string; hint: string }> = {
  whatYoureBuilding: { title: "What You're Building", icon: Lightbulb, color: 'var(--cash)', hint: 'e.g. "Make it a Chrome extension instead"' },
  whoWantsThis: { title: 'Who Wants This', icon: Users, color: 'var(--gold)', hint: 'e.g. "Target teachers, not designers"' },
  whyThisCouldWork: { title: 'Why This Could Work', icon: Sparkles, color: 'var(--blue)', hint: 'e.g. "Focus more on the pricing angle"' },
  coreFeatures: { title: 'Core Features', icon: Layers, color: 'var(--cash)', hint: 'e.g. "Add Stripe billing" or "Remove the chat feature"' },
  techStack: { title: 'Tech Stack', icon: Wrench, color: 'var(--brown-muted)', hint: 'e.g. "Use Firebase instead of Supabase"' },
  buildPlan: { title: 'Build Plan', icon: ListOrdered, color: 'var(--cash-dark)', hint: 'e.g. "Make it simpler, fewer steps"' },
  v2Ideas: { title: 'What V2 Looks Like', icon: Rocket, color: 'var(--blue)', hint: 'e.g. "Add a mobile app for V2"' },
  risks: { title: 'Honest Risks', icon: AlertTriangle, color: 'var(--gold)', hint: 'e.g. "What about competitors like X?"' },
}

function specToMarkdown(spec: BuildSpec): string {
  const lines: string[] = [
    `# ${spec.productName} -- Build Spec`,
    '',
    '## What You\'re Building',
    '', spec.whatYoureBuilding, '',
    '## Who Wants This and Why',
    '', spec.whoWantsThis, '',
    '## Why This Could Work',
    '', spec.whyThisCouldWork, '',
    '## Core Features (MVP Only)',
    '', ...spec.coreFeatures.map((f) => `- ${f}`), '',
    '## Tech Stack',
    '', ...spec.techStack.map((t) => `- **${t.tool}** -- ${t.why}`), '',
    '## Build Plan',
    '', ...spec.buildPlan.map((s, i) => `${i + 1}. ${s}`), '',
    '## What V2 Looks Like',
    '', ...spec.v2Ideas.map((v) => `- ${v}`), '',
    '## Honest Risks',
    '', ...spec.risks.map((r) => `- ${r}`), '',
    `**Estimated complexity:** ${COMPLEXITY_CONFIG[spec.complexity]?.label || spec.complexity}`,
    '', '---', '',
    'Generated by Cash Cow. Paste this into Claude Code and start building.',
  ]
  return lines.join('\n')
}

export default function BuildSpecPage() {
  const params = useParams()
  const router = useRouter()
  const id = params.id as string
  const hasStartedGeneration = useRef(false)

  const [idea, setIdea] = useState<ProductIdea | null>(null)
  const [spec, setSpec] = useState<BuildSpec | null>(null)
  const [loading, setLoading] = useState(false)
  const [remixing, setRemixing] = useState(false)
  const [error, setError] = useState('')
  const [copied, setCopied] = useState(false)
  const [showStartGuide, setShowStartGuide] = useState(false)
  const [remixCount, setRemixCount] = useState(0)
  const [changedSections, setChangedSections] = useState<Set<SectionKey>>(new Set())

  // Per-section remix state
  const [activeRemixSection, setActiveRemixSection] = useState<SectionKey | null>(null)
  const [sectionRemixInput, setSectionRemixInput] = useState('')

  // Global remix bar
  const [globalRemixInput, setGlobalRemixInput] = useState('')
  const [globalRemixFocused, setGlobalRemixFocused] = useState(false)

  const remixInputRef = useRef<HTMLTextAreaElement>(null)
  const globalInputRef = useRef<HTMLTextAreaElement>(null)

  // Load idea
  useEffect(() => {
    const tempIdea = getTempIdea()
    const favIdea = getFavoriteById(id)
    const found = tempIdea?.id === id ? tempIdea : favIdea
    if (found) {
      setIdea(found)
      const cached = getCachedSpec(found.id)
      if (cached) setSpec(cached)
    }
  }, [id])

  // Auto-generate on load
  const generateSpec = useCallback(async () => {
    if (!idea || hasStartedGeneration.current) return
    hasStartedGeneration.current = true
    setLoading(true)
    setError('')

    try {
      const res = await fetch('/api/build-spec', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ idea }),
      })
      if (!res.ok) {
        const data = await res.json()
        throw new Error(data.error || 'Failed to generate spec')
      }
      const data = await res.json()
      setSpec(data.spec)
      addFavorite(idea)
      cacheSpec(idea.id, data.spec)
    } catch (err) {
      setError(err instanceof Error ? err.message : 'Something went wrong')
      hasStartedGeneration.current = false
    } finally {
      setLoading(false)
    }
  }, [idea])

  useEffect(() => {
    if (idea && !spec && !loading && !error) generateSpec()
  }, [idea, spec, loading, error, generateSpec])

  // Remix function
  async function handleRemix(instruction: string, sectionHint?: string) {
    if (!spec || !instruction.trim()) return

    setRemixing(true)
    setError('')
    setChangedSections(new Set())

    try {
      const res = await fetch('/api/remix-spec', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ spec, instruction, section: sectionHint }),
      })
      if (!res.ok) {
        const data = await res.json()
        throw new Error(data.error || 'Failed to remix')
      }
      const data = await res.json()
      const newSpec = data.spec as BuildSpec

      // Detect which sections changed
      const changed = new Set<SectionKey>()
      const keys: SectionKey[] = ['whatYoureBuilding', 'whoWantsThis', 'whyThisCouldWork', 'coreFeatures', 'techStack', 'buildPlan', 'v2Ideas', 'risks']
      for (const key of keys) {
        if (JSON.stringify(newSpec[key]) !== JSON.stringify(spec[key])) {
          changed.add(key)
        }
      }

      setSpec(newSpec)
      setChangedSections(changed)
      setRemixCount((c) => c + 1)
      setActiveRemixSection(null)
      setSectionRemixInput('')
      setGlobalRemixInput('')

      // Cache updated spec
      if (idea) cacheSpec(idea.id, newSpec)

      // Clear changed highlights after animation
      setTimeout(() => setChangedSections(new Set()), 2000)
    } catch (err) {
      setError(err instanceof Error ? err.message : 'Remix failed')
    } finally {
      setRemixing(false)
    }
  }

  function handleSectionClick(key: SectionKey) {
    if (remixing) return
    if (activeRemixSection === key) {
      setActiveRemixSection(null)
      setSectionRemixInput('')
    } else {
      setActiveRemixSection(key)
      setSectionRemixInput('')
      setTimeout(() => remixInputRef.current?.focus(), 50)
    }
  }

  function handleSectionRemixSubmit(e: React.FormEvent) {
    e.preventDefault()
    if (!sectionRemixInput.trim() || !activeRemixSection) return
    handleRemix(sectionRemixInput, SECTION_META[activeRemixSection].title)
  }

  function handleGlobalRemixSubmit(e: React.FormEvent) {
    e.preventDefault()
    if (!globalRemixInput.trim()) return
    handleRemix(globalRemixInput)
  }

  async function handleCopy() {
    if (!spec) return
    await navigator.clipboard.writeText(specToMarkdown(spec))
    setCopied(true)
    setTimeout(() => setCopied(false), 2000)
  }

  function handleRetry() {
    hasStartedGeneration.current = false
    setError('')
    generateSpec()
  }

  // Not found
  if (!idea) {
    return (
      <div className="min-h-screen flex flex-col items-center justify-center" style={{ background: 'var(--cream)' }}>
        <p className="text-lg font-bold mb-4" style={{ fontFamily: 'var(--font-fredoka), sans-serif' }}>
          Idea not found
        </p>
        <p className="text-sm mb-6" style={{ color: 'var(--brown-muted)' }}>
          Go back and pick an idea to build.
        </p>
        <button
          onClick={() => router.push('/ideas')}
          className="btn-bounce inline-flex items-center gap-2 px-5 py-2.5 rounded-xl font-bold"
          style={{ background: 'var(--cash)', color: '#fff' }}
        >
          <ArrowLeft size={16} />
          Back to ideas
        </button>
      </div>
    )
  }

  const complexityInfo = spec ? COMPLEXITY_CONFIG[spec.complexity] : null

  return (
    <div className="min-h-screen" style={{ background: 'var(--cream)' }}>
      <div className="px-4 sm:px-6 pt-6 pb-16">
        <div className="max-w-3xl mx-auto">
          {/* Back + remix count */}
          <div className="flex items-center justify-between mb-6">
            <button
              onClick={() => router.push('/ideas')}
              className="animate-in inline-flex items-center gap-1.5 text-sm font-bold transition-colors"
              style={{ color: 'var(--brown-muted)' }}
            >
              <ArrowLeft size={14} />
              Back to ideas
            </button>
            {remixCount > 0 && (
              <span
                className="remix-badge inline-flex items-center gap-1.5 px-3 py-1 rounded-full text-xs font-bold"
                style={{ background: 'var(--cash-soft)', color: 'var(--cash-dark)' }}
              >
                <RefreshCw size={12} />
                {remixCount} remix{remixCount !== 1 ? 'es' : ''}
              </span>
            )}
          </div>

          {/* Idea header */}
          <div
            className="animate-in rounded-2xl p-6 mb-6"
            style={{
              background: 'var(--white)',
              border: '2px solid var(--spot-gray)',
              boxShadow: '0 4px 20px rgba(41, 37, 36, 0.06)',
              animationDelay: '0.05s',
            }}
          >
            <div className="flex items-center gap-2 mb-3 flex-wrap">
              <span className="px-2.5 py-1 rounded-lg text-xs font-bold" style={{ background: 'var(--cream-dark)', color: 'var(--brown-muted)' }}>
                {idea.category}
              </span>
              <span
                className="inline-flex items-center gap-1 px-2.5 py-1 rounded-lg text-xs font-bold"
                style={{ background: COMPLEXITY_CONFIG[idea.complexity].bg, color: COMPLEXITY_CONFIG[idea.complexity].color }}
              >
                <Clock size={11} />
                {COMPLEXITY_CONFIG[idea.complexity].label}
              </span>
            </div>
            <h1 className="text-2xl sm:text-3xl font-bold mb-2" style={{ fontFamily: 'var(--font-fredoka), sans-serif' }}>
              {idea.name}
            </h1>
            <p className="text-sm" style={{ color: 'var(--brown-muted)' }}>{idea.pitch}</p>
          </div>

          {/* Loading */}
          {loading && (
            <div
              className="animate-in rounded-2xl p-12 text-center"
              style={{ background: 'var(--white)', border: '2px solid var(--spot-gray)' }}
            >
              <Loader2 size={40} className="animate-spin mx-auto mb-4" style={{ color: 'var(--cash)' }} />
              <p className="text-lg font-bold mb-1" style={{ fontFamily: 'var(--font-fredoka), sans-serif' }}>
                Writing your build plan...
              </p>
              <p className="text-sm mb-1" style={{ color: 'var(--brown-muted)' }}>
                About 15 seconds. This is a real spec, not a template.
              </p>
              <p className="text-xs" style={{ color: 'var(--brown-faint)' }}>
                You&apos;ll be able to remix every section after.
              </p>

              <div className="mt-8 space-y-4 text-left">
                {['What You\'re Building', 'Who Wants This', 'Core Features', 'Build Plan'].map((section) => (
                  <div key={section} className="rounded-xl p-4" style={{ background: 'var(--cream)', border: '1px solid var(--spot-gray)' }}>
                    <div className="h-3 w-32 rounded mb-3" style={{ background: 'var(--spot-gray)' }} />
                    <div className="space-y-2">
                      <div className="h-2.5 rounded" style={{ background: 'var(--spot-gray)', width: '90%' }} />
                      <div className="h-2.5 rounded" style={{ background: 'var(--spot-gray)', width: '70%' }} />
                    </div>
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Error */}
          {error && (
            <div className="rounded-2xl p-6 mb-6" style={{ background: 'var(--red-soft)', border: '2px solid rgba(239, 68, 68, 0.2)' }}>
              <p className="font-bold mb-1" style={{ color: 'var(--red)' }}>Something went wrong</p>
              <p className="text-sm mb-4" style={{ color: 'var(--brown-muted)' }}>{error}</p>
              <button onClick={handleRetry} className="btn-bounce px-4 py-2 rounded-xl text-sm font-bold" style={{ background: 'var(--red)', color: '#fff' }}>
                Try Again
              </button>
            </div>
          )}

          {/* Spec display + remix */}
          {spec && (
            <div className="space-y-4">
              {/* Global remix bar */}
              <div
                className={`animate-in rounded-2xl p-4 ${globalRemixFocused || globalRemixInput ? 'remix-bar-active' : ''}`}
                style={{
                  background: 'var(--white)',
                  border: `2px solid ${globalRemixFocused || globalRemixInput ? 'var(--cash)' : 'var(--spot-gray)'}`,
                  boxShadow: globalRemixFocused ? '0 4px 20px rgba(34, 197, 94, 0.1)' : '0 4px 20px rgba(41, 37, 36, 0.06)',
                  transition: 'border-color 0.2s, box-shadow 0.2s',
                }}
              >
                <form onSubmit={handleGlobalRemixSubmit}>
                  <div className="flex items-center gap-2 mb-2">
                    <RefreshCw size={14} style={{ color: 'var(--cash)' }} />
                    <span className="text-xs font-bold uppercase tracking-wider" style={{ color: 'var(--brown-muted)' }}>
                      Remix this spec
                    </span>
                  </div>
                  <div className="flex gap-2">
                    <textarea
                      ref={globalInputRef}
                      value={globalRemixInput}
                      onChange={(e) => setGlobalRemixInput(e.target.value)}
                      onFocus={() => setGlobalRemixFocused(true)}
                      onBlur={() => setGlobalRemixFocused(false)}
                      onKeyDown={(e) => {
                        if (e.key === 'Enter' && !e.shiftKey) {
                          e.preventDefault()
                          handleGlobalRemixSubmit(e)
                        }
                      }}
                      placeholder="Tell me what to change... &quot;Make it a mobile app&quot; or &quot;Target restaurant owners instead&quot;"
                      className="flex-1 bg-transparent outline-none text-sm resize-none"
                      style={{ color: 'var(--brown)', minHeight: '40px', maxHeight: '120px' }}
                      rows={1}
                      disabled={remixing}
                    />
                    <button
                      type="submit"
                      disabled={!globalRemixInput.trim() || remixing}
                      className="btn-bounce self-end inline-flex items-center gap-1.5 px-4 py-2 rounded-xl text-sm font-bold flex-shrink-0 disabled:opacity-40 disabled:cursor-not-allowed"
                      style={{
                        background: globalRemixInput.trim() ? 'var(--cash)' : 'var(--cream-dark)',
                        color: globalRemixInput.trim() ? '#fff' : 'var(--brown-faint)',
                        transition: 'background 0.2s, color 0.2s',
                      }}
                    >
                      {remixing ? (
                        <Loader2 size={14} className="animate-spin" />
                      ) : (
                        <Send size={14} />
                      )}
                      Remix
                    </button>
                  </div>
                </form>
              </div>

              {/* Remixing overlay hint */}
              {remixing && (
                <div className="flex items-center justify-center gap-2 py-2">
                  <span className="w-2 h-2 rounded-full pulse-dot-1" style={{ background: 'var(--cash)' }} />
                  <span className="w-2 h-2 rounded-full pulse-dot-2" style={{ background: 'var(--cash)' }} />
                  <span className="w-2 h-2 rounded-full pulse-dot-3" style={{ background: 'var(--cash)' }} />
                  <span className="text-sm font-bold ml-1" style={{ color: 'var(--cash)' }}>Remixing your spec...</span>
                </div>
              )}

              {/* Spec header */}
              <div
                className="animate-in rounded-2xl p-6"
                style={{
                  background: 'var(--white)',
                  border: '2px solid var(--spot-gray)',
                  boxShadow: '0 4px 20px rgba(41, 37, 36, 0.06)',
                }}
              >
                <div className="flex items-center justify-between gap-4 mb-4 flex-wrap">
                  <h2 className="text-2xl font-bold" style={{ fontFamily: 'var(--font-fredoka), sans-serif' }}>
                    {spec.productName}
                  </h2>
                  {complexityInfo && (
                    <span
                      className="inline-flex items-center gap-1.5 px-3 py-1.5 rounded-xl text-xs font-bold"
                      style={{ background: complexityInfo.bg, color: complexityInfo.color }}
                    >
                      <complexityInfo.icon size={14} />
                      {complexityInfo.label}
                    </span>
                  )}
                </div>
                <p className="text-xs mb-4" style={{ color: 'var(--brown-faint)' }}>
                  Click any section below to remix it, or use the remix bar above for big changes.
                </p>
                <button
                  onClick={handleCopy}
                  className="btn-bounce inline-flex items-center gap-2 px-4 py-2 rounded-xl text-sm font-bold"
                  style={{
                    background: copied ? 'var(--cash-soft)' : 'var(--spot-black)',
                    color: copied ? 'var(--cash)' : 'var(--cream)',
                    border: copied ? '2px solid var(--cash)' : '2px solid var(--spot-black)',
                  }}
                >
                  {copied ? <Check size={14} /> : <Copy size={14} />}
                  {copied ? 'Copied!' : 'Copy Spec as Markdown'}
                </button>
              </div>

              {/* Remixable sections */}
              {(Object.keys(SECTION_META) as SectionKey[]).map((key) => (
                <RemixableSection
                  key={key}
                  sectionKey={key}
                  spec={spec}
                  meta={SECTION_META[key]}
                  isActive={activeRemixSection === key}
                  isRemixing={remixing}
                  isChanged={changedSections.has(key)}
                  remixInput={activeRemixSection === key ? sectionRemixInput : ''}
                  onRemixInputChange={setSectionRemixInput}
                  onSectionClick={() => handleSectionClick(key)}
                  onRemixSubmit={handleSectionRemixSubmit}
                  onCancel={() => { setActiveRemixSection(null); setSectionRemixInput('') }}
                  inputRef={activeRemixSection === key ? remixInputRef : undefined}
                />
              ))}

              {/* Bottom CTAs */}
              <div
                className="rounded-2xl p-6 text-center space-y-4"
                style={{
                  background: 'var(--white)',
                  border: '2px solid var(--cash)',
                  boxShadow: '0 4px 20px rgba(34, 197, 94, 0.1)',
                }}
              >
                <div className="flex items-center justify-center gap-3 flex-wrap">
                  <button
                    onClick={handleCopy}
                    className="btn-bounce inline-flex items-center gap-2 px-6 py-3 rounded-xl text-base font-bold"
                    style={{
                      background: 'var(--spot-black)',
                      color: 'var(--cream)',
                      boxShadow: '0 4px 20px rgba(28, 25, 23, 0.15)',
                    }}
                  >
                    {copied ? <Check size={16} /> : <Copy size={16} />}
                    {copied ? 'Copied!' : 'Copy Spec as Markdown'}
                  </button>
                  <button
                    onClick={() => setShowStartGuide(!showStartGuide)}
                    className="btn-bounce inline-flex items-center gap-2 px-6 py-3 rounded-xl text-base font-bold"
                    style={{
                      background: 'var(--cash)',
                      color: '#fff',
                      boxShadow: '0 4px 14px rgba(34, 197, 94, 0.3)',
                    }}
                  >
                    <Terminal size={16} />
                    Start Building
                  </button>
                </div>

                {showStartGuide && (
                  <div className="text-left rounded-xl p-5 mt-4" style={{ background: 'var(--spot-black)', color: 'var(--cream)' }}>
                    <p className="text-sm font-bold mb-3" style={{ color: 'var(--cash-light)' }}>What to do right now:</p>
                    <ol className="space-y-2 text-sm" style={{ color: 'var(--cream-dark)' }}>
                      <li className="flex items-start gap-2">
                        <span className="font-bold" style={{ color: 'var(--cash-light)' }}>1.</span>
                        Open your terminal. Run{' '}
                        <code className="px-1.5 py-0.5 rounded text-xs" style={{ background: 'rgba(255,255,255,0.1)' }}>
                          npx create-next-app@latest {spec.productName.toLowerCase().replace(/\s+/g, '-')}
                        </code>
                      </li>
                      <li className="flex items-start gap-2">
                        <span className="font-bold" style={{ color: 'var(--cash-light)' }}>2.</span>
                        Click &quot;Copy Spec as Markdown&quot; and paste it into Claude Code.
                      </li>
                      <li className="flex items-start gap-2">
                        <span className="font-bold" style={{ color: 'var(--cash-light)' }}>3.</span>
                        Start with build step #1. Go one step at a time.
                      </li>
                    </ol>
                  </div>
                )}

                <p className="text-xs" style={{ color: 'var(--brown-faint)' }}>
                  Paste the spec into Claude Code, Cursor, or any AI coding tool.
                </p>
              </div>
            </div>
          )}
        </div>
      </div>
    </div>
  )
}

// -- Remixable Section Component --

function RemixableSection({
  sectionKey,
  spec,
  meta,
  isActive,
  isRemixing,
  isChanged,
  remixInput,
  onRemixInputChange,
  onSectionClick,
  onRemixSubmit,
  onCancel,
  inputRef,
}: {
  readonly sectionKey: SectionKey
  readonly spec: BuildSpec
  readonly meta: { title: string; icon: React.ElementType; color: string; hint: string }
  readonly isActive: boolean
  readonly isRemixing: boolean
  readonly isChanged: boolean
  readonly remixInput: string
  readonly onRemixInputChange: (v: string) => void
  readonly onSectionClick: () => void
  readonly onRemixSubmit: (e: React.FormEvent) => void
  readonly onCancel: () => void
  readonly inputRef?: React.RefObject<HTMLTextAreaElement | null>
}) {
  const Icon = meta.icon

  return (
    <div
      className={`rounded-2xl p-5 ${isRemixing ? 'remix-shimmer' : 'section-remixable'} ${isChanged ? 'remix-changed' : ''}`}
      style={{
        background: 'var(--white)',
        border: `2px solid ${isActive ? 'var(--cash)' : 'var(--spot-gray)'}`,
        boxShadow: isActive ? '0 4px 20px rgba(34, 197, 94, 0.08)' : '0 4px 20px rgba(41, 37, 36, 0.04)',
        transition: 'border-color 0.2s, box-shadow 0.2s',
      }}
    >
      {/* Header -- clickable */}
      <div
        className="flex items-center gap-2 mb-3 cursor-pointer group"
        onClick={onSectionClick}
      >
        <Icon size={16} style={{ color: meta.color }} />
        <h3 className="text-sm font-bold uppercase tracking-wider flex-1" style={{ color: 'var(--brown-muted)' }}>
          {meta.title}
        </h3>
        <span
          className="inline-flex items-center gap-1 px-2 py-0.5 rounded-lg text-xs font-bold opacity-0 group-hover:opacity-100 transition-opacity"
          style={{ background: 'var(--cash-soft)', color: 'var(--cash)' }}
        >
          <Pencil size={10} />
          Remix
        </span>
      </div>

      {/* Content */}
      <SectionContent sectionKey={sectionKey} spec={spec} />

      {/* Inline remix input */}
      {isActive && (
        <form onSubmit={onRemixSubmit} className="mt-4 pt-4" style={{ borderTop: '1px solid var(--spot-gray)' }}>
          <div className="flex gap-2">
            <textarea
              ref={inputRef}
              value={remixInput}
              onChange={(e) => onRemixInputChange(e.target.value)}
              onKeyDown={(e) => {
                if (e.key === 'Enter' && !e.shiftKey) {
                  e.preventDefault()
                  onRemixSubmit(e)
                }
              }}
              placeholder={meta.hint}
              className="remix-input flex-1 bg-transparent outline-none text-sm resize-none rounded-xl px-3 py-2"
              style={{
                color: 'var(--brown)',
                background: 'var(--cream)',
                border: '1px solid var(--spot-gray)',
                minHeight: '40px',
              }}
              rows={2}
              disabled={isRemixing}
            />
            <div className="flex flex-col gap-1.5 self-end">
              <button
                type="submit"
                disabled={!remixInput.trim() || isRemixing}
                className="btn-bounce inline-flex items-center gap-1 px-3 py-2 rounded-xl text-xs font-bold disabled:opacity-40"
                style={{ background: 'var(--cash)', color: '#fff' }}
              >
                {isRemixing ? <Loader2 size={12} className="animate-spin" /> : <Send size={12} />}
                Go
              </button>
              <button
                type="button"
                onClick={onCancel}
                className="inline-flex items-center justify-center px-3 py-1.5 rounded-xl text-xs"
                style={{ color: 'var(--brown-faint)' }}
              >
                <X size={12} />
              </button>
            </div>
          </div>
        </form>
      )}
    </div>
  )
}

