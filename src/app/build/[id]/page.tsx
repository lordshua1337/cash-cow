'use client'

import { useState, useEffect, useCallback } from 'react'
import { useParams, useRouter } from 'next/navigation'
import {
  ArrowLeft,
  Hammer,
  Loader2,
  Copy,
  Check,
  Clock,
  Calendar,
  CalendarRange,
  Wrench,
  Users,
  Lightbulb,
  Layers,
  ListOrdered,
} from 'lucide-react'
import type { TrendingProduct, BuildSpec } from '@/lib/types'
import { getFavoriteById } from '@/lib/favorites'

const COMPLEXITY_CONFIG = {
  weekend: { label: 'Weekend Project', icon: Clock, color: 'var(--cash)', bg: 'var(--cash-soft)' },
  'few-weeks': { label: 'A Few Weeks', icon: Calendar, color: 'var(--gold)', bg: 'var(--gold-soft)' },
  'month-plus': { label: 'A Month+', icon: CalendarRange, color: 'var(--blue)', bg: 'var(--blue-soft)' },
} as const

function specToMarkdown(spec: BuildSpec, product: TrendingProduct): string {
  const lines: string[] = [
    `# ${spec.productName}`,
    '',
    `> Build spec generated from ${product.source === 'hackernews' ? 'Hacker News' : 'GitHub'} trending: [${product.title}](${product.url})`,
    '',
    '## What It Does',
    '',
    spec.whatItDoes,
    '',
    '## Who It\'s For',
    '',
    spec.whoItsFor,
    '',
    '## Core Features (MVP)',
    '',
    ...spec.coreFeatures.map((f) => `- ${f}`),
    '',
    '## Tech Stack',
    '',
    ...spec.techStack.map((t) => `- ${t}`),
    '',
    '## MVP Scope',
    '',
    spec.mvpScope,
    '',
    `**Estimated complexity:** ${COMPLEXITY_CONFIG[spec.complexity]?.label || spec.complexity}`,
    '',
    '## Build Steps',
    '',
    ...spec.buildSteps.map((s, i) => `${i + 1}. ${s}`),
    '',
    '---',
    '',
    `Generated by Cash Cow from ${product.source === 'hackernews' ? 'Hacker News' : 'GitHub'} trending data.`,
  ]
  return lines.join('\n')
}

export default function BuildSpecPage() {
  const params = useParams()
  const router = useRouter()
  const id = params.id as string

  const [product, setProduct] = useState<TrendingProduct | null>(null)
  const [spec, setSpec] = useState<BuildSpec | null>(null)
  const [loading, setLoading] = useState(false)
  const [error, setError] = useState('')
  const [copied, setCopied] = useState(false)

  useEffect(() => {
    const found = getFavoriteById(id)
    if (found) {
      setProduct(found)
    }
  }, [id])

  const generateSpec = useCallback(async () => {
    if (!product) return
    setLoading(true)
    setError('')
    setSpec(null)

    try {
      const res = await fetch('/api/build-spec', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ product }),
      })

      if (!res.ok) {
        const data = await res.json()
        throw new Error(data.error || 'Failed to generate spec')
      }

      const data = await res.json()
      setSpec(data.spec)
    } catch (err) {
      setError(err instanceof Error ? err.message : 'Something went wrong')
    } finally {
      setLoading(false)
    }
  }, [product])

  async function handleCopy() {
    if (!spec || !product) return
    const md = specToMarkdown(spec, product)
    await navigator.clipboard.writeText(md)
    setCopied(true)
    setTimeout(() => setCopied(false), 2000)
  }

  if (!product) {
    return (
      <div className="min-h-screen flex flex-col items-center justify-center" style={{ background: 'var(--cream)' }}>
        <p className="text-lg font-bold mb-4" style={{ fontFamily: 'var(--font-fredoka), sans-serif' }}>
          Product not found
        </p>
        <p className="text-sm mb-6" style={{ color: 'var(--brown-muted)' }}>
          This product might have been removed from your saves.
        </p>
        <button
          onClick={() => router.push('/favorites')}
          className="btn-bounce inline-flex items-center gap-2 px-5 py-2.5 rounded-xl font-bold"
          style={{ background: 'var(--cash)', color: '#fff' }}
        >
          <ArrowLeft size={16} />
          Back to Favorites
        </button>
      </div>
    )
  }

  const complexityInfo = spec ? COMPLEXITY_CONFIG[spec.complexity] : null

  return (
    <div className="min-h-screen" style={{ background: 'var(--cream)' }}>
      <div className="px-4 sm:px-6 pt-6 pb-16">
        <div className="max-w-3xl mx-auto">
          {/* Back link */}
          <button
            onClick={() => router.push('/favorites')}
            className="animate-in inline-flex items-center gap-1.5 text-sm font-bold mb-6 transition-colors"
            style={{ color: 'var(--brown-muted)' }}
          >
            <ArrowLeft size={14} />
            Back to Favorites
          </button>

          {/* Product header */}
          <div
            className="animate-in rounded-2xl p-6 mb-6"
            style={{
              background: 'var(--white)',
              border: '2px solid var(--spot-gray)',
              boxShadow: '0 4px 20px rgba(41, 37, 36, 0.06)',
              animationDelay: '0.05s',
            }}
          >
            <div className="flex items-start justify-between gap-4 mb-3">
              <div>
                <div
                  className="inline-flex items-center gap-1.5 px-2.5 py-1 rounded-lg text-xs font-bold mb-2"
                  style={{
                    background: product.source === 'hackernews' ? 'var(--gold-soft)' : 'var(--blue-soft)',
                    color: product.source === 'hackernews' ? 'var(--gold)' : 'var(--blue)',
                  }}
                >
                  {product.source === 'hackernews' ? 'Hacker News' : 'GitHub'}
                </div>
                <h1
                  className="text-2xl sm:text-3xl font-bold mb-1"
                  style={{ fontFamily: 'var(--font-fredoka), sans-serif' }}
                >
                  {product.title}
                </h1>
                <p className="text-sm" style={{ color: 'var(--brown-muted)' }}>
                  {product.description}
                </p>
              </div>
            </div>

            {!spec && !loading && (
              <button
                onClick={generateSpec}
                className="btn-bounce inline-flex items-center gap-2 px-6 py-3 rounded-xl text-base font-bold mt-2"
                style={{
                  background: 'var(--cash)',
                  color: '#fff',
                  boxShadow: '0 4px 14px rgba(34, 197, 94, 0.3)',
                }}
              >
                <Hammer size={18} />
                Generate Build Spec
              </button>
            )}
          </div>

          {/* Loading */}
          {loading && (
            <div
              className="animate-in rounded-2xl p-12 text-center"
              style={{
                background: 'var(--white)',
                border: '2px solid var(--spot-gray)',
              }}
            >
              <Loader2
                size={40}
                className="animate-spin mx-auto mb-4"
                style={{ color: 'var(--cash)' }}
              />
              <p
                className="text-lg font-bold mb-1"
                style={{ fontFamily: 'var(--font-fredoka), sans-serif' }}
              >
                Building your spec...
              </p>
              <p className="text-sm" style={{ color: 'var(--brown-muted)' }}>
                This takes about 10-15 seconds. We&apos;re writing a real build plan, not a template.
              </p>
            </div>
          )}

          {/* Error */}
          {error && (
            <div
              className="rounded-2xl p-6 mb-6"
              style={{
                background: 'var(--red-soft)',
                border: '2px solid rgba(239, 68, 68, 0.2)',
              }}
            >
              <p className="font-bold mb-1" style={{ color: 'var(--red)' }}>
                Something went wrong
              </p>
              <p className="text-sm mb-4" style={{ color: 'var(--brown-muted)' }}>
                {error}
              </p>
              <button
                onClick={generateSpec}
                className="btn-bounce px-4 py-2 rounded-xl text-sm font-bold"
                style={{ background: 'var(--red)', color: '#fff' }}
              >
                Try Again
              </button>
            </div>
          )}

          {/* Spec display */}
          {spec && (
            <div className="space-y-4">
              {/* Spec header with copy button */}
              <div
                className="animate-in rounded-2xl p-6"
                style={{
                  background: 'var(--white)',
                  border: '2px solid var(--spot-gray)',
                  boxShadow: '0 4px 20px rgba(41, 37, 36, 0.06)',
                }}
              >
                <div className="flex items-center justify-between mb-4">
                  <h2
                    className="text-2xl font-bold"
                    style={{ fontFamily: 'var(--font-fredoka), sans-serif' }}
                  >
                    {spec.productName}
                  </h2>
                  {complexityInfo && (
                    <div
                      className="inline-flex items-center gap-1.5 px-3 py-1.5 rounded-xl text-xs font-bold"
                      style={{ background: complexityInfo.bg, color: complexityInfo.color }}
                    >
                      <complexityInfo.icon size={14} />
                      {complexityInfo.label}
                    </div>
                  )}
                </div>

                <button
                  onClick={handleCopy}
                  className="btn-bounce inline-flex items-center gap-2 px-4 py-2 rounded-xl text-sm font-bold"
                  style={{
                    background: copied ? 'var(--cash-soft)' : 'var(--spot-black)',
                    color: copied ? 'var(--cash)' : 'var(--cream)',
                    border: copied ? '2px solid var(--cash)' : '2px solid var(--spot-black)',
                  }}
                >
                  {copied ? <Check size={14} /> : <Copy size={14} />}
                  {copied ? 'Copied as Markdown!' : 'Copy Spec for Claude'}
                </button>
              </div>

              {/* What it does */}
              <SpecSection
                icon={Lightbulb}
                title="What it does"
                color="var(--cash)"
              >
                <p className="text-sm leading-relaxed" style={{ color: 'var(--brown-light)' }}>
                  {spec.whatItDoes}
                </p>
              </SpecSection>

              {/* Who it's for */}
              <SpecSection
                icon={Users}
                title="Who it's for"
                color="var(--gold)"
              >
                <p className="text-sm leading-relaxed" style={{ color: 'var(--brown-light)' }}>
                  {spec.whoItsFor}
                </p>
              </SpecSection>

              {/* Core features */}
              <SpecSection
                icon={Layers}
                title="Core features"
                color="var(--blue)"
              >
                <ul className="space-y-2">
                  {spec.coreFeatures.map((feature, i) => (
                    <li key={i} className="flex items-start gap-2 text-sm" style={{ color: 'var(--brown-light)' }}>
                      <span
                        className="flex-shrink-0 w-5 h-5 rounded-md flex items-center justify-center text-xs font-bold mt-0.5"
                        style={{ background: 'var(--blue-soft)', color: 'var(--blue)' }}
                      >
                        {i + 1}
                      </span>
                      {feature}
                    </li>
                  ))}
                </ul>
              </SpecSection>

              {/* Tech stack */}
              <SpecSection
                icon={Wrench}
                title="Tech stack"
                color="var(--brown-muted)"
              >
                <div className="flex flex-wrap gap-2">
                  {spec.techStack.map((tech, i) => (
                    <span
                      key={i}
                      className="inline-flex px-3 py-1.5 rounded-lg text-xs font-bold"
                      style={{
                        background: 'var(--cream-dark)',
                        color: 'var(--brown-light)',
                        border: '1px solid var(--spot-gray)',
                      }}
                    >
                      {tech}
                    </span>
                  ))}
                </div>
              </SpecSection>

              {/* MVP scope */}
              <SpecSection
                icon={Hammer}
                title="MVP scope"
                color="var(--cash)"
              >
                <p className="text-sm leading-relaxed" style={{ color: 'var(--brown-light)' }}>
                  {spec.mvpScope}
                </p>
              </SpecSection>

              {/* Build steps */}
              <SpecSection
                icon={ListOrdered}
                title="Build steps"
                color="var(--gold)"
              >
                <ol className="space-y-3">
                  {spec.buildSteps.map((step, i) => (
                    <li key={i} className="flex items-start gap-3 text-sm" style={{ color: 'var(--brown-light)' }}>
                      <span
                        className="flex-shrink-0 w-6 h-6 rounded-full flex items-center justify-center text-xs font-bold"
                        style={{ background: 'var(--gold-soft)', color: 'var(--gold)' }}
                      >
                        {i + 1}
                      </span>
                      <span className="pt-0.5">{step}</span>
                    </li>
                  ))}
                </ol>
              </SpecSection>

              {/* Bottom copy button */}
              <div className="text-center pt-4">
                <button
                  onClick={handleCopy}
                  className="btn-bounce inline-flex items-center gap-2 px-6 py-3 rounded-xl text-base font-bold"
                  style={{
                    background: 'var(--spot-black)',
                    color: 'var(--cream)',
                    boxShadow: '0 4px 20px rgba(28, 25, 23, 0.15)',
                  }}
                >
                  {copied ? <Check size={16} /> : <Copy size={16} />}
                  {copied ? 'Copied!' : 'Copy Spec as Markdown'}
                </button>
                <p className="text-xs mt-3" style={{ color: 'var(--brown-faint)' }}>
                  Paste this into Claude Code and start building
                </p>
              </div>
            </div>
          )}
        </div>
      </div>
    </div>
  )
}

function SpecSection({
  icon: Icon,
  title,
  color,
  children,
}: {
  readonly icon: React.ElementType
  readonly title: string
  readonly color: string
  readonly children: React.ReactNode
}) {
  return (
    <div
      className="animate-in rounded-2xl p-5"
      style={{
        background: 'var(--white)',
        border: '2px solid var(--spot-gray)',
        boxShadow: '0 4px 20px rgba(41, 37, 36, 0.04)',
      }}
    >
      <div className="flex items-center gap-2 mb-3">
        <Icon size={16} style={{ color }} />
        <h3
          className="text-sm font-bold uppercase tracking-wider"
          style={{ color: 'var(--brown-muted)' }}
        >
          {title}
        </h3>
      </div>
      {children}
    </div>
  )
}
