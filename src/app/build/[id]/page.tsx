'use client'

import { useState, useEffect, useCallback, useRef } from 'react'
import { useParams, useRouter } from 'next/navigation'
import {
  ArrowLeft,
  Loader2,
  Copy,
  Check,
  Clock,
  Calendar,
  CalendarRange,
  Wrench,
  Users,
  Lightbulb,
  Layers,
  ListOrdered,
  AlertTriangle,
  Rocket,
  Terminal,
  Zap,
  Sparkles,
  Heart,
} from 'lucide-react'
import type { ProductIdea, BuildSpec } from '@/lib/types'
import {
  getTempIdea,
  getFavoriteById,
  addFavorite,
  isFavorite,
  getCachedSpec,
  cacheSpec,
} from '@/lib/favorites'

const COMPLEXITY_CONFIG = {
  weekend: { label: 'Weekend Project', icon: Clock, color: 'var(--cash)', bg: 'var(--cash-soft)' },
  'few-weeks': { label: 'A Few Weeks', icon: Calendar, color: 'var(--gold)', bg: 'var(--gold-soft)' },
  'month-plus': { label: 'A Month+', icon: CalendarRange, color: 'var(--blue)', bg: 'var(--blue-soft)' },
} as const

function specToMarkdown(spec: BuildSpec): string {
  const lines: string[] = [
    `# ${spec.productName} -- Build Spec`,
    '',
    '## What You\'re Building',
    '',
    spec.whatYoureBuilding,
    '',
    '## Who Wants This and Why',
    '',
    spec.whoWantsThis,
    '',
    '## Why This Could Work',
    '',
    spec.whyThisCouldWork,
    '',
    '## Core Features (MVP Only)',
    '',
    ...spec.coreFeatures.map((f) => `- ${f}`),
    '',
    '## Tech Stack',
    '',
    ...spec.techStack.map((t) => `- **${t.tool}** -- ${t.why}`),
    '',
    '## Build Plan',
    '',
    ...spec.buildPlan.map((s, i) => `${i + 1}. ${s}`),
    '',
    '## What V2 Looks Like',
    '',
    ...spec.v2Ideas.map((v) => `- ${v}`),
    '',
    '## Honest Risks',
    '',
    ...spec.risks.map((r) => `- ${r}`),
    '',
    `**Estimated complexity:** ${COMPLEXITY_CONFIG[spec.complexity]?.label || spec.complexity}`,
    '',
    '---',
    '',
    'Generated by Cash Cow. Paste this into Claude Code and start building.',
  ]
  return lines.join('\n')
}

export default function BuildSpecPage() {
  const params = useParams()
  const router = useRouter()
  const id = params.id as string
  const hasStartedGeneration = useRef(false)

  const [idea, setIdea] = useState<ProductIdea | null>(null)
  const [spec, setSpec] = useState<BuildSpec | null>(null)
  const [loading, setLoading] = useState(false)
  const [error, setError] = useState('')
  const [copied, setCopied] = useState(false)
  const [saved, setSaved] = useState(false)
  const [showStartGuide, setShowStartGuide] = useState(false)

  // Load idea from temp storage or favorites
  useEffect(() => {
    const tempIdea = getTempIdea()
    const favIdea = getFavoriteById(id)
    const found = tempIdea?.id === id ? tempIdea : favIdea

    if (found) {
      setIdea(found)
      setSaved(isFavorite(found.id))

      // Check for cached spec
      const cached = getCachedSpec(found.id)
      if (cached) {
        setSpec(cached)
      }
    }
  }, [id])

  // Auto-generate spec on load (if not cached)
  const generateSpec = useCallback(async () => {
    if (!idea || hasStartedGeneration.current) return
    hasStartedGeneration.current = true

    setLoading(true)
    setError('')

    try {
      const res = await fetch('/api/build-spec', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ idea }),
      })

      if (!res.ok) {
        const data = await res.json()
        throw new Error(data.error || 'Failed to generate spec')
      }

      const data = await res.json()
      setSpec(data.spec)

      // Auto-save idea + cache spec
      addFavorite(idea)
      setSaved(true)
      cacheSpec(idea.id, data.spec)
    } catch (err) {
      setError(err instanceof Error ? err.message : 'Something went wrong')
      hasStartedGeneration.current = false
    } finally {
      setLoading(false)
    }
  }, [idea])

  // Trigger generation when idea is loaded and no cached spec
  useEffect(() => {
    if (idea && !spec && !loading && !error) {
      generateSpec()
    }
  }, [idea, spec, loading, error, generateSpec])

  async function handleCopyMarkdown() {
    if (!spec) return
    const md = specToMarkdown(spec)
    await navigator.clipboard.writeText(md)
    setCopied(true)
    setTimeout(() => setCopied(false), 2000)
  }

  function handleSave() {
    if (!idea) return
    addFavorite(idea)
    if (spec) cacheSpec(idea.id, spec)
    setSaved(true)
  }

  function handleRetry() {
    hasStartedGeneration.current = false
    setError('')
    generateSpec()
  }

  if (!idea) {
    return (
      <div className="min-h-screen flex flex-col items-center justify-center" style={{ background: 'var(--cream)' }}>
        <p className="text-lg font-bold mb-4" style={{ fontFamily: 'var(--font-fredoka), sans-serif' }}>
          Idea not found
        </p>
        <p className="text-sm mb-6" style={{ color: 'var(--brown-muted)' }}>
          Go back and pick an idea to build.
        </p>
        <button
          onClick={() => router.push('/')}
          className="btn-bounce inline-flex items-center gap-2 px-5 py-2.5 rounded-xl font-bold"
          style={{ background: 'var(--cash)', color: '#fff' }}
        >
          <ArrowLeft size={16} />
          Back to Ideas
        </button>
      </div>
    )
  }

  const complexityInfo = spec ? COMPLEXITY_CONFIG[spec.complexity] : null

  return (
    <div className="min-h-screen" style={{ background: 'var(--cream)' }}>
      <div className="px-4 sm:px-6 pt-6 pb-16">
        <div className="max-w-3xl mx-auto">
          {/* Back link */}
          <button
            onClick={() => router.push('/')}
            className="animate-in inline-flex items-center gap-1.5 text-sm font-bold mb-6 transition-colors"
            style={{ color: 'var(--brown-muted)' }}
          >
            <ArrowLeft size={14} />
            Back to Ideas
          </button>

          {/* Idea summary header */}
          <div
            className="animate-in rounded-2xl p-6 mb-6"
            style={{
              background: 'var(--white)',
              border: '2px solid var(--spot-gray)',
              boxShadow: '0 4px 20px rgba(41, 37, 36, 0.06)',
              animationDelay: '0.05s',
            }}
          >
            <div className="flex items-center gap-2 mb-3 flex-wrap">
              <span
                className="px-2.5 py-1 rounded-lg text-xs font-bold"
                style={{ background: 'var(--cream-dark)', color: 'var(--brown-muted)' }}
              >
                {idea.category}
              </span>
              <span
                className="inline-flex items-center gap-1 px-2.5 py-1 rounded-lg text-xs font-bold"
                style={{
                  background: COMPLEXITY_CONFIG[idea.complexity].bg,
                  color: COMPLEXITY_CONFIG[idea.complexity].color,
                }}
              >
                <Clock size={11} />
                {COMPLEXITY_CONFIG[idea.complexity].label}
              </span>
            </div>
            <h1
              className="text-2xl sm:text-3xl font-bold mb-2"
              style={{ fontFamily: 'var(--font-fredoka), sans-serif' }}
            >
              {idea.name}
            </h1>
            <p className="text-sm" style={{ color: 'var(--brown-muted)' }}>
              {idea.pitch}
            </p>
          </div>

          {/* Loading state */}
          {loading && (
            <div
              className="animate-in rounded-2xl p-12 text-center"
              style={{
                background: 'var(--white)',
                border: '2px solid var(--spot-gray)',
              }}
            >
              <Loader2
                size={40}
                className="animate-spin mx-auto mb-4"
                style={{ color: 'var(--cash)' }}
              />
              <p
                className="text-lg font-bold mb-1"
                style={{ fontFamily: 'var(--font-fredoka), sans-serif' }}
              >
                Writing your build plan...
              </p>
              <p className="text-sm" style={{ color: 'var(--brown-muted)' }}>
                About 15 seconds. This is a real spec, not a template.
              </p>

              {/* Skeleton sections */}
              <div className="mt-8 space-y-4 text-left">
                {['What You\'re Building', 'Who Wants This', 'Why This Could Work', 'Core Features', 'Build Plan'].map((section) => (
                  <div
                    key={section}
                    className="rounded-xl p-4"
                    style={{ background: 'var(--cream)', border: '1px solid var(--spot-gray)' }}
                  >
                    <div
                      className="h-3 w-32 rounded mb-3"
                      style={{ background: 'var(--spot-gray)' }}
                    />
                    <div className="space-y-2">
                      <div className="h-2.5 rounded" style={{ background: 'var(--spot-gray)', width: '90%' }} />
                      <div className="h-2.5 rounded" style={{ background: 'var(--spot-gray)', width: '75%' }} />
                      <div className="h-2.5 rounded" style={{ background: 'var(--spot-gray)', width: '60%' }} />
                    </div>
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Error */}
          {error && (
            <div
              className="rounded-2xl p-6 mb-6"
              style={{
                background: 'var(--red-soft)',
                border: '2px solid rgba(239, 68, 68, 0.2)',
              }}
            >
              <p className="font-bold mb-1" style={{ color: 'var(--red)' }}>
                Something went wrong
              </p>
              <p className="text-sm mb-4" style={{ color: 'var(--brown-muted)' }}>
                {error}
              </p>
              <button
                onClick={handleRetry}
                className="btn-bounce px-4 py-2 rounded-xl text-sm font-bold"
                style={{ background: 'var(--red)', color: '#fff' }}
              >
                Try Again
              </button>
            </div>
          )}

          {/* Spec display */}
          {spec && (
            <div className="space-y-4">
              {/* Spec header */}
              <div
                className="animate-in rounded-2xl p-6"
                style={{
                  background: 'var(--white)',
                  border: '2px solid var(--spot-gray)',
                  boxShadow: '0 4px 20px rgba(41, 37, 36, 0.06)',
                }}
              >
                <div className="flex items-center justify-between gap-4 mb-4 flex-wrap">
                  <h2
                    className="text-2xl font-bold"
                    style={{ fontFamily: 'var(--font-fredoka), sans-serif' }}
                  >
                    {spec.productName}
                  </h2>
                  {complexityInfo && (
                    <span
                      className="inline-flex items-center gap-1.5 px-3 py-1.5 rounded-xl text-xs font-bold"
                      style={{ background: complexityInfo.bg, color: complexityInfo.color }}
                    >
                      <complexityInfo.icon size={14} />
                      {complexityInfo.label}
                    </span>
                  )}
                </div>

                <div className="flex items-center gap-2 flex-wrap">
                  <button
                    onClick={handleCopyMarkdown}
                    className="btn-bounce inline-flex items-center gap-2 px-4 py-2 rounded-xl text-sm font-bold"
                    style={{
                      background: copied ? 'var(--cash-soft)' : 'var(--spot-black)',
                      color: copied ? 'var(--cash)' : 'var(--cream)',
                      border: copied ? '2px solid var(--cash)' : '2px solid var(--spot-black)',
                    }}
                  >
                    {copied ? <Check size={14} /> : <Copy size={14} />}
                    {copied ? 'Copied!' : 'Copy Spec as Markdown'}
                  </button>

                  {!saved && (
                    <button
                      onClick={handleSave}
                      className="btn-bounce inline-flex items-center gap-1.5 px-4 py-2 rounded-xl text-sm font-bold"
                      style={{
                        background: 'var(--cream-dark)',
                        color: 'var(--brown-muted)',
                        border: '2px solid var(--spot-gray)',
                      }}
                    >
                      <Heart size={14} />
                      Save to Favorites
                    </button>
                  )}
                </div>
              </div>

              {/* What you're building */}
              <SpecSection icon={Lightbulb} title="What You're Building" color="var(--cash)">
                <p className="text-sm leading-relaxed" style={{ color: 'var(--brown-light)' }}>
                  {spec.whatYoureBuilding}
                </p>
              </SpecSection>

              {/* Who wants this */}
              <SpecSection icon={Users} title="Who Wants This and Why" color="var(--gold)">
                <p className="text-sm leading-relaxed" style={{ color: 'var(--brown-light)' }}>
                  {spec.whoWantsThis}
                </p>
              </SpecSection>

              {/* Why this could work */}
              <SpecSection icon={Sparkles} title="Why This Could Work" color="var(--blue)">
                <p className="text-sm leading-relaxed" style={{ color: 'var(--brown-light)' }}>
                  {spec.whyThisCouldWork}
                </p>
              </SpecSection>

              {/* Core features */}
              <SpecSection icon={Layers} title="Core Features (MVP Only)" color="var(--cash)">
                <ul className="space-y-2">
                  {spec.coreFeatures.map((feature, i) => (
                    <li key={i} className="flex items-start gap-2 text-sm" style={{ color: 'var(--brown-light)' }}>
                      <span
                        className="flex-shrink-0 w-5 h-5 rounded-md flex items-center justify-center text-xs font-bold mt-0.5"
                        style={{ background: 'var(--cash-soft)', color: 'var(--cash)' }}
                      >
                        {i + 1}
                      </span>
                      {feature}
                    </li>
                  ))}
                </ul>
              </SpecSection>

              {/* Tech stack */}
              <SpecSection icon={Wrench} title="Tech Stack" color="var(--brown-muted)">
                <div className="space-y-2">
                  {spec.techStack.map((tech, i) => (
                    <div
                      key={i}
                      className="flex items-start gap-3 text-sm"
                      style={{ color: 'var(--brown-light)' }}
                    >
                      <span
                        className="inline-flex px-2.5 py-1 rounded-lg text-xs font-bold flex-shrink-0"
                        style={{
                          background: 'var(--cream-dark)',
                          color: 'var(--brown)',
                          border: '1px solid var(--spot-gray)',
                        }}
                      >
                        {tech.tool}
                      </span>
                      <span className="pt-0.5" style={{ color: 'var(--brown-muted)' }}>
                        {tech.why}
                      </span>
                    </div>
                  ))}
                </div>
              </SpecSection>

              {/* Build plan */}
              <SpecSection icon={ListOrdered} title="Build Plan" color="var(--cash-dark)">
                <ol className="space-y-3">
                  {spec.buildPlan.map((step, i) => (
                    <li key={i} className="flex items-start gap-3 text-sm" style={{ color: 'var(--brown-light)' }}>
                      <span
                        className="flex-shrink-0 w-6 h-6 rounded-full flex items-center justify-center text-xs font-bold"
                        style={{ background: 'var(--cash-soft)', color: 'var(--cash-dark)' }}
                      >
                        {i + 1}
                      </span>
                      <span className="pt-0.5 leading-relaxed">{step}</span>
                    </li>
                  ))}
                </ol>
              </SpecSection>

              {/* V2 ideas */}
              <SpecSection icon={Rocket} title="What V2 Looks Like" color="var(--blue)">
                <ul className="space-y-2">
                  {spec.v2Ideas.map((v, i) => (
                    <li key={i} className="flex items-start gap-2 text-sm" style={{ color: 'var(--brown-light)' }}>
                      <span
                        className="flex-shrink-0 w-5 h-5 rounded-md flex items-center justify-center text-xs font-bold mt-0.5"
                        style={{ background: 'var(--blue-soft)', color: 'var(--blue)' }}
                      >
                        {i + 1}
                      </span>
                      {v}
                    </li>
                  ))}
                </ul>
              </SpecSection>

              {/* Honest risks */}
              <SpecSection icon={AlertTriangle} title="Honest Risks" color="var(--gold)">
                <ul className="space-y-2">
                  {spec.risks.map((risk, i) => (
                    <li key={i} className="flex items-start gap-2 text-sm" style={{ color: 'var(--brown-light)' }}>
                      <AlertTriangle size={14} className="flex-shrink-0 mt-0.5" style={{ color: 'var(--gold)' }} />
                      {risk}
                    </li>
                  ))}
                </ul>
              </SpecSection>

              {/* Bottom CTAs */}
              <div
                className="rounded-2xl p-6 text-center space-y-4"
                style={{
                  background: 'var(--white)',
                  border: '2px solid var(--cash)',
                  boxShadow: '0 4px 20px rgba(34, 197, 94, 0.1)',
                }}
              >
                <div className="flex items-center justify-center gap-3 flex-wrap">
                  <button
                    onClick={handleCopyMarkdown}
                    className="btn-bounce inline-flex items-center gap-2 px-6 py-3 rounded-xl text-base font-bold"
                    style={{
                      background: 'var(--spot-black)',
                      color: 'var(--cream)',
                      boxShadow: '0 4px 20px rgba(28, 25, 23, 0.15)',
                    }}
                  >
                    {copied ? <Check size={16} /> : <Copy size={16} />}
                    {copied ? 'Copied!' : 'Copy Spec as Markdown'}
                  </button>

                  <button
                    onClick={() => setShowStartGuide(!showStartGuide)}
                    className="btn-bounce inline-flex items-center gap-2 px-6 py-3 rounded-xl text-base font-bold"
                    style={{
                      background: 'var(--cash)',
                      color: '#fff',
                      boxShadow: '0 4px 14px rgba(34, 197, 94, 0.3)',
                    }}
                  >
                    <Terminal size={16} />
                    Start Building
                  </button>
                </div>

                {showStartGuide && (
                  <div
                    className="text-left rounded-xl p-5 mt-4"
                    style={{
                      background: 'var(--spot-black)',
                      color: 'var(--cream)',
                    }}
                  >
                    <p className="text-sm font-bold mb-3" style={{ color: 'var(--cash-light)' }}>
                      What to do right now:
                    </p>
                    <ol className="space-y-2 text-sm" style={{ color: 'var(--cream-dark)' }}>
                      <li className="flex items-start gap-2">
                        <span className="font-bold" style={{ color: 'var(--cash-light)' }}>1.</span>
                        Open your terminal. Run{' '}
                        <code
                          className="px-1.5 py-0.5 rounded text-xs"
                          style={{ background: 'rgba(255,255,255,0.1)' }}
                        >
                          npx create-next-app@latest {spec.productName.toLowerCase().replace(/\s+/g, '-')}
                        </code>
                      </li>
                      <li className="flex items-start gap-2">
                        <span className="font-bold" style={{ color: 'var(--cash-light)' }}>2.</span>
                        Click &quot;Copy Spec as Markdown&quot; above and paste it into Claude Code.
                      </li>
                      <li className="flex items-start gap-2">
                        <span className="font-bold" style={{ color: 'var(--cash-light)' }}>3.</span>
                        Start with build step #1 above. Go one step at a time.
                      </li>
                    </ol>
                  </div>
                )}

                <p className="text-xs" style={{ color: 'var(--brown-faint)' }}>
                  Paste the spec into Claude Code, Cursor, or any AI coding tool.
                </p>
              </div>
            </div>
          )}
        </div>
      </div>
    </div>
  )
}

function SpecSection({
  icon: Icon,
  title,
  color,
  children,
}: {
  readonly icon: React.ElementType
  readonly title: string
  readonly color: string
  readonly children: React.ReactNode
}) {
  return (
    <div
      className="animate-in rounded-2xl p-5"
      style={{
        background: 'var(--white)',
        border: '2px solid var(--spot-gray)',
        boxShadow: '0 4px 20px rgba(41, 37, 36, 0.04)',
      }}
    >
      <div className="flex items-center gap-2 mb-3">
        <Icon size={16} style={{ color }} />
        <h3
          className="text-sm font-bold uppercase tracking-wider"
          style={{ color: 'var(--brown-muted)' }}
        >
          {title}
        </h3>
      </div>
      {children}
    </div>
  )
}
